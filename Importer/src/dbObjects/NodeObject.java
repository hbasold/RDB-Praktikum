package dbObjects;

public class NodeObject {

private String id;
private String lat;
private String lon;
public void setId(String id) {
	this.id = id;
}
public String getId() {
	return id;
}
public void setLat(String lat) {
	this.lat = lat;
}
public String getLat() {
	return lat;
}
public void setLon(String lon) {
	this.lon = lon;
}
public String getLon() {
	return lon;
}
}
